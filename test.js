let a = prompt('Please input the first number:');
let b = prompt('Please input the second number:');

let add = (Number(a) + Number(b));
let sub = (Number(a) - Number(b));
let mult = (Number(a) * Number(b));
let div = (Number(a) / Number(b));

alert(`${a} + ${b} = ${add}`);
alert(`${a} - ${b} = ${sub}`);
alert(`${a} * ${b} = ${mult}`);
alert(`${a} / ${b} = ${div}`);
